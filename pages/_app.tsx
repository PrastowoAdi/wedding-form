import React, { ReactNode } from "react";

import Head from "next/head";
import { AppProps } from "next/app";
import { Provider } from "react-redux";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { ReactQueryDevtools } from "@tanstack/react-query-devtools";
import { PersistGate } from "redux-persist/integration/react";
import { NextPage } from "next";
import { ToastContainer } from "react-toastify";

import { store, persistor } from "@/store";
import Layout from "@/layout";
import { Navbar, Sidebar } from "@/components";

import "react-toastify/dist/ReactToastify.css";
import "../styles/globals.css";
import "react-datepicker/dist/react-datepicker.css";

type Page<P = {}> = NextPage<P> & {
  getLayout?: (page: ReactNode) => ReactNode;
};

type Props = AppProps & {
  Component: Page;
};

const App = ({ Component, pageProps }: Props) => {
  const [queryClient] = React.useState(() => new QueryClient());
  const getLayout =
    Component.getLayout ??
    ((page) => (
      <Layout>
        <Sidebar />
        <div className="relative md:ml-64 bg-slate-600">
          <Navbar />
          <div
            id="main-content"
            className="absolute w-full px-4 mx-auto md:px-10 top-10 md:top-20"
          >
            <div className="flex flex-wrap">
              <div className="w-full mb-12 xl:mb-0">{page}</div>
            </div>
          </div>
        </div>
      </Layout>
    ));

  return (
    <>
      <Provider store={store}>
        <PersistGate loading={null} persistor={persistor}>
          <QueryClientProvider client={queryClient}>
            <ReactQueryDevtools />
            <>
              <Head>
                <title>Divory Wedding Form</title>
                <meta name="description" content="Generated by Divory Team." />
              </Head>
              {getLayout(<Component {...pageProps} />)}
              <ToastContainer />
            </>
          </QueryClientProvider>
        </PersistGate>
      </Provider>
    </>
  );
};

export default App;
